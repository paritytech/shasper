use crate::*;
use primitive_types::*;

#[test]
fn serialize_uint() {
	assert_eq!(&U256::from(659821).encode()[..], b"m\x11\n\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00");
	assert_eq!(&362452341234145u128.encode()[..], b"\xe1\x19\x0c\x03\xa6I\x01\x00\x00\x00\x00\x00\x00\x00\x00\x00");
	assert_eq!(&8474385234u64.encode()[..], b"R\xdb\x1c\xf9\x01\x00\x00\x00");
	assert_eq!(&76465737u32.encode()[..], b"I\xc6\x8e\x04");
	assert_eq!(&52437u16.encode()[..], b"\xd5\xcc");
}
